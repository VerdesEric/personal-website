                        <p><b>Unclear Iconography</b>: Users struggled with icons like “Feed” and “Add User,” indicating the need for more intuitive visual cues.</p>
                    </div>
                    <div class="problems2">
                        <h4>02.</h4>
                        <p><b>Visual Engagement Gaps</b>: The app’s current design lacks visual appeal, limiting user satisfaction and long-term engagement.</p>
                    </div>
                    <div class="problems2">
                        <h4>03.</h4>
                        <p><b>Restricted Personalization</b>: Users couldn’t scan their own rooms, reducing the ability to create tailored and immersive design experiences.</p>
                    </div>
                    <div class="problems2">
                        <h4>04.</h4>
                        <p><b>Interaction Flow Confusion</b>: Navigation issues slowed task completion, showing a need for clearer, more streamlined user journeys.</p>
