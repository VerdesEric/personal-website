                <h4>Mid-Fidelity Prototype</h4>
                <p>The mid-fidelity prototype marked a key step in <b>refining our concept</b>, helping us apply best practices in user interface design and better visualize the final product. Built in Figma, this prototype allowed us to simulate <b>real app navigation</b>, offering a more <b>interactive and structured experience</b> than the paper version. It helped us test layout, flow, and usability in a realistic format, ensuring that each feature—from AR scanning to AI suggestions—was intuitive, functional, and aligned with users’ expectations.</p>
