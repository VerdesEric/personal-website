    <div class="design-proj2">
        <h3>What it is</h3>
        <p>Room Vision is an interactive design solution that combines modular furniture with a digital platform tailored for <b>busy college students</b>. Designed with functionality, personalization, and sustainability in mind, the system includes an <b>AR-enhanced</b> mobile app with <b>AI-driven</b> suggestions and a <b>collaborative community feed</b>. Room Vision empowers students to personalize their living spaces <b>efficiently</b>—even with limited time and space—without compromising on comfort or aesthetics.</p>
        <h3>Design Challenge</h3>
        <p class="inline">How might we help college students personalize small living spaces in a way that balances aesthetics, comfort, and practical constraints—despite time pressures and space limitations?</p>
        <p>Inspired by the real needs of student life, we tackled the everyday problem of <b>functional, comfortable design inspiration</b> in limited campus housing. Room Vision addresses this by providing a flexible system of <b>eco-friendly modular furniture</b> paired with a <b>digital interface</b> that not only guides users through their room design journey but also serves as a source of inspiration—helping students discover, explore, and implement creative design decisions that suit their <b>space and lifestyle</b>.</p>
        <h3>The Vision</h3>
        <p>The goal wasn’t just to provide furniture—it was to create a system where digital tools enhance the <b>physical environment</b>. We envisioned a smart assistant for student living: an app that uses augmented reality to visualize layouts, artificial intelligence to suggest personalized styles, and community engagement to promote shared design ideas.</p>
        <p>Room Vision helps transform daily environments into spaces that support student well-being, productivity, and creative self-expression.</p>
