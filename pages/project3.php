    <div class="design-proj3">
        <h3>Overview</h3>
        <p>In many e-commerce platforms, there’s a growing disconnect between the <b>customer experience and the administrative systems</b> behind it. Consumers expect fast, intuitive browsing and real-time updates, while administrators often face time constraints and outdated tools that make managing product entries slow and inefficient. This gap not only <b>delays updates</b> but <b>weakens the relationship between users and those managing their experience</b>. Bridging this divide requires a platform that serves both sides—one that simplifies backend workflows while keeping the customer experience seamless, current, and engaging.</p>
        <h3>The Challenge</h3>
        <p class="inline">How might we design a single platform that delivers a seamless, intuitive shopping experience for users while equipping administrators with powerful, efficient tools to manage it all behind the scenes?</p>
        <p>Tennis House was born from a challenge I experienced firsthand as a tennis player: the disconnect between a smooth retail experience and the clunky, outdated systems that power it behind the scenes. I saw this disconnect as an <b>opportunity</b> to design a solution that didn’t force a tradeoff between <b>usability and administrative control</b>.</p>
        <h3>Solution</h3>
        <p><b>A dual-platform eCommerce experience for tennis enthusiasts and administrators</b>. A responsive website that streamlines gear discovery for players while giving admins a fast, intuitive way to manage product entries in real time—bridging <b>front-end satisfaction with efficient backend control</b>.</p>
